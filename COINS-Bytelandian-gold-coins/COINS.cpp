ll rec(ll num){
    if(num==0)return 0;
    if(num==1)return 1;
    if(dp.count(num))return dp[num];
    ll ret=num;
    ret=max(ret, rec(num/2)+rec(num/3)+rec(num/4));
    dp[num]=ret;
    return dp[num];
}

